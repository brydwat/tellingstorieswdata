# Focused: Often having an aim to “learn R” or similar tends to be problematic,
# because there is no real end point to that. It tends to be more efficient to
# have smaller, more specific goals, such as “make a histogram about the 2022
# Australian Election with ggplot2”. This is something that can be focused on 
# and achieved in a few hours. The issue with goals that are more nebulous, 
# such as “I want to learn R”, is that it is easier to get lost on tangents 
# and more difficult to get help. This can be demoralizing and lead to people 
# quitting too early.

